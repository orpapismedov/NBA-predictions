// Returns the stat leader — threshold is 66% of the current average GP (auto-adjusts as season progresses)
export type StatKey = keyof PlayerStats

export function getStatLeader(key: StatKey, lowerIsBetter = false): Player {
  const avgGP = MOCK_PLAYERS.reduce((s, p) => s + p.currentStats.gp, 0) / MOCK_PLAYERS.length
  const threshold = Math.round(avgGP * 0.66)
  const eligible = MOCK_PLAYERS.filter(p => p.currentStats.gp >= threshold)
  if (!eligible.length) return MOCK_PLAYERS[0]
  return eligible.reduce((best, player) => {
    const a = player.currentStats[key] as number
    const b = best.currentStats[key] as number
    return lowerIsBetter ? (a < b ? player : best) : (a > b ? player : best)
  })
}

export function searchPlayers(query: string): Player[] {
  const q = query.toLowerCase()
  return MOCK_PLAYERS.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.team.toLowerCase().includes(q) ||
    p.teamAbbr.toLowerCase().includes(q) ||
    p.position.toLowerCase().includes(q)
  )
}

export function getPlayer(id: string): Player | undefined {
  return MOCK_PLAYERS.find(p => p.id === id)
}

export const getPlayerById = getPlayer
